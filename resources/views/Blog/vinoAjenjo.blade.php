					<p>La salud es el resultado de la perfecta obediencia a las leyes naturales que rigen nuestro ser. En favor de esto, LOPE MESTAS, SUSY BEATRIZ y MAMANI PACCO, DELIA DEL RIO&nbsp; nuestras egresadas e investigadoras,&nbsp; presentan este trabajo. El ajenjo es una planta de muchos beneficios para la salud, dentro de ellos combatir la parasitosis o enfermedad parasitaria.&nbsp; Aquí el resultado:</p>

<p>VINO MEDICINAL DE AJENJO</p>

<p>(Artemisia absinthium)</p>

<p>COMPOSICION: Ajenjo seco…………………..10g</p>

<p>Vino blanco………………….100ml</p>

<p>ACCION TERAPEUTICA Elimina parásitos intestinales sobre todo en niños (principalmente oxiuros). USO RECOMENDADO Niños: 1 cucharadita 3 veces al día después de los alimentos. Adultos. 2 ml 3 veces al día después de los alimentos.</p>

<p>PRECAUCIONES: no tiene efecto adverso por seguridad no administrar durante el embarazo.</p>

<p>ADVERTENCIA: ninguna en caso de algún efecto no deseado interrumpir el tratamiento.</p>

<p>VIA DE ADMINISTRACION Vía oral</p>

<p>CONDICIONES DE ALMACENAMIENTO Almacenar en un lugar fresco y seco.</p>

<p>LABORATORIO SAN JUAN BAUTISTA LA SALLE Delia Del Rio Mamani Pacco Susy Beatriz Lope mestas.</p>

<p>Las propiedades farmacológicas del Ajenjo hacen que esta planta sea aplicada en problemas de parasitosis específicamente en oxiuros, pues su acción es inhibitoria en larvas de los mismos. El principio activo que presenta el ajenjo es la tuyona y la absentina, el primero actúa como agente antiparasitario, este componente actúa básicamente sobre la membrana celular de los oxiuros debilitándola y el segundo lo inactiva.</p>

