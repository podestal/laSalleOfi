					<p>Las enfermedades parasitarias intestinales tienen una distribución mundial, sin embargo, son más comunes en áreas rurales o urbano marginales, constituyéndose en un problema de salud pública para los habitantes de esas regiones. En general estas infecciones son consideradas como un marcador de atraso socio-cultural. Son más frecuentes en países subdesarrollados, siendo la población infantil la más susceptible debido a su inmadurez inmunológica y al poco desarrollo de hábitos higiénicos. La baja mortalidad en comparación a la elevada morbilidad es el aspecto más problemático de las parasitosis intestinales y hace difícil estimar de forma adecuada la carga de las enteroparasitosis como problema de salud pública, realizándose en muchos casos sub-estimaciones de este problema.</p>

<p>Como parte del trabajo de investigación, se hicieron análisis en el barrio Túpac Amaru de la ciudad de Juliaca, a 220 niños, con una media de edad de 6.1 años, y se obtuvo que la prevalencia de parásitos intestinales fue de 78.9% (56/71).</p>

<p>La elevada prevalencia de parasitosis intestinales determinada (78.9%) coincide con aquellas obtenidas por otros autores en niños de diferentes regiones de la Región Puno. Por todo ello, es de suma importancia, contar con políticas de salud, y a su vez, proponer alternativas de solución proporcionando medicamentos de origen natural como las plantas medicinales con que cuenta nuestra región Puno, por ello es que MAMANI HAÑARI, Heydy Nadier y CHAMBI MAMANI, Yolanda</p>

<p>&nbsp;Propusieron&nbsp; el trabajo de investigación “EL SAUCO COMO ANTIPARASITARIO PARA LOS NIÑOS DE LOS BARRIOS URBANOS MARGINADOS DE LA CIUDAD DE JULIACA 2010”.</p>

